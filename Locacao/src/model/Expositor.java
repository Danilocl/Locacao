package model;

public class Expositor {

	private String ende;
	private String ljFisica;
	private int idUser;
	private String nomeUser;
	private String nomeFantasy;


	public String getEnde() {
		return ende;
	}

	public void setEnde(String ende) {
		this.ende = ende;
	}

	public String getLjFisica() {
		return ljFisica;
	}

	public void setLjFisica(String ljFisica) {
		this.ljFisica = ljFisica;
	}

	public int getIdUser() {
		return idUser;
	}

	public void setIdUser(int idUser) {
		this.idUser = idUser;
	}

	public String getNomeUser() {
		return nomeUser;
	}

	public void setNomeUser(String nomeUser) {
		this.nomeUser = nomeUser;
	}

	public String getNomeFantasy() {
		return nomeFantasy;
	}

	public void setNomeFantasy(String nomeFantasy) {
		this.nomeFantasy = nomeFantasy;
	}

}
